#include "libft.h"

char	*ft_strncat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t j;

	i = 0;
	j = 0;
	while (dst[i])
		i++;
	while (src[j] && j < size)
	{
		dst[j + i] = src[j];
		j++;
	}
	dst[j + i] = '\0';
	return (dst);
}
