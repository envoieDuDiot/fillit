#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int j;

	j = 0;
	while (src[j] != '\0')
	{
		dst[j] = src[j];
		j++;
	}
	dst[j] = '\0';
	return (dst);
}
